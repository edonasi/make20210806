//*--------------------------------------------------------------------------------*
//								コンストラクタ
//*--------------------------------------------------------------------------------*

ClCircle::ClCircle(){
	center = n_xy::ClXY();
	radius = 0;
}
ClCircle::~ClCircle() { return; }

//*--------------------------------------------------------------------------------*
//								プロパティ
//*--------------------------------------------------------------------------------*

VOID ClCircle::SetPos(int x, int y) { center.SetXY(x, y); }

VOID ClCircle::SetRadius(int radius_in) { radius = radius_in; }

VOID ClCircle::SetCircle(int x, int y, int radius_in) {
	center.SetXY(x, y);
	radius = radius_in;
}